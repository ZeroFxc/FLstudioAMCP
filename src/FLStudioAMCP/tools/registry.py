def get_tool(tool_id: str) -> dict[str, Any] | None:
    """根据 tool_id 获取工具定义。

    Args:
        tool_id: 工具唯一标识，如 "transport.play"

    Returns:
        工具定义字典，如果未找到则返回 None
    """
    return TOOL_REGISTRY.get(tool_id)


def search_tools(query: str, category: str | None = None) -> list[dict[str, Any]]:
    """按名称和描述搜索工具，返回匹配的工具列表。

    搜索范围包括工具 ID、名称和描述字段。
    匹配不区分大小写。

    Args:
        query: 搜索关键词
        category: 可选，按分类筛选（如 "transport"、"mixer" 等）

    Returns:
        匹配的工具定义列表
    """
    query_lower = query.lower()
    results: list[dict[str, Any]] = []

    for tool in TOOL_REGISTRY.values():
        # 分类筛选
        if category is not None and tool.get("category") != category:
            continue

        # 搜索匹配
        if (
            query_lower in tool["id"].lower()
            or query_lower in tool["name"].lower()
            or query_lower in tool["description"].lower()
        ):
            results.append(tool)

    return results