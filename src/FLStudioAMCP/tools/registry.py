from __future__ import annotations

from typing import Any


# =============================================================================
# 辅助函数：简化工具定义
# =============================================================================


def _def(
    tool_id: str,
    name: str,
    description: str,
    category: str,
    *,
    action: str | None = None,
    handler: str | None = None,
    params: dict[str, dict[str, Any]] | None = None,
    param_map: dict[str, str] | None = None,
    module: str | None = None,
    function: str | None = None,
    args: list[str] | None = None,
) -> dict[str, Any]:
    """构建单个工具定义条目。

    Args:
        tool_id: 工具唯一标识，如 "transport.play"
        name: 中文显示名称
        description: 中文功能描述
        category: 分类名
        action: MIDI action 字符串，如 "transport.start" 或 "flapi.call"
        handler: 特殊处理器标记，如 "piano_roll"（与 action 互斥）
        params: 参数定义字典，key 为 MCP 参数名（蛇形），value 为 {"type": ..., "required": ..., "default": ..., "description": ...}
        param_map: MCP 参数名 → action 参数名的映射
        module: flapi.call 使用的 FL Studio API 模块名，如 "patterns"
        function: flapi.call 使用的 FL Studio API 函数名，如 "patternCount"
        args: 参数名到位置参数的顺序映射列表，如 ["pattern_index", "name"]

    Returns:
        工具定义字典
    """
    tool_def: dict[str, Any] = {
        "id": tool_id,
        "name": name,
        "description": description,
        "category": category,
        "params": params or {},
    }
    if action is not None:
        tool_def["action"] = action
    if handler is not None:
        tool_def["handler"] = handler
    if param_map is not None:
        tool_def["param_map"] = param_map
    if module is not None:
        tool_def["module"] = module
    if function is not None:
        tool_def["function"] = function
    if args is not None:
        tool_def["args"] = args
    return tool_def


